import path from 'path';
import { copyFile, stat } from 'fs/promises';
import { cp } from 'fs/promises';

/**
 * Copy a file only if the destination doesn't exist or is older than the source.
 * Avoids unnecessary overwrites that can invalidate caches.
 * Uses mtime comparison for efficiency.
 */
async function smartCopyFile(src: string, dest: string): Promise<void> {
	try {
		const [srcStat, destStat] = await Promise.all([
			stat(src).catch(() => null),
			stat(dest).catch(() => null)
		]);

		if (!srcStat) {
			throw new Error(`Source file does not exist: ${src}`);
		}

		if (!destStat) {
			// Destination doesn't exist, copy
			console.log(`[build] Copying ${dest} (new file)`);
			await copyFile(src, dest);
			return;
		}

		// Copy only if source is newer than destination
		if (srcStat.mtime > destStat.mtime) {
			console.log(`[build] Copying ${dest} (updated)`);
			await copyFile(src, dest);
			return;
		}
		console.log(`[build] Skipping ${dest} (unchanged)`);
	} catch (error) {
		// Fallback to regular copy on error
		console.log(`[build] Copying ${dest} (fallback)`);
		await copyFile(src, dest);
	}
}

/**
 * Copy a directory only if the source is newer than the destination.
 * Falls back to full copy on any error.
 */
async function smartCopyDir(src: string, dest: string): Promise<void> {
	try {
		const [srcStat, destStat] = await Promise.all([
			stat(src).catch(() => null),
			stat(dest).catch(() => null)
		]);
		if (!srcStat || !destStat || srcStat.mtime > destStat.mtime) {
			console.log(`[build] Copying ${dest}`);
			await cp(src, dest, { recursive: true });
		} else {
			console.log(`[build] Skipping ${dest} (unchanged)`);
		}
	} catch {
		console.log(`[build] Copying ${dest} (fallback)`);
		await cp(src, dest, { recursive: true });
	}
}

export async function copyMonacoAssets(
	pluginDir: string,
	buildPath: string
): Promise<void> {
	// Monaco core
	await smartCopyDir(
		path.join(pluginDir, 'node_modules/monaco-editor/min/vs'),
		path.join(buildPath, 'vs')
	);
	// Codicons font (CSP blocks data: sources in child iframes)
	await smartCopyFile(
		path.join(
			pluginDir,
			'node_modules/monaco-editor/esm/vs/base/browser/ui/codicons/codicon/codicon.ttf'
		),
		path.join(buildPath, 'vs/editor/codicon.ttf')
	);
	// Monaco themes
	await smartCopyDir(
		path.join(pluginDir, 'node_modules/monaco-themes/themes'),
		path.join(buildPath, 'monaco-themes')
	);
}

export async function copyEditorFiles(
	pluginDir: string,
	buildPath: string
): Promise<void> {
	const files: [string, string][] = [
		['src/editor/monacoEditor.html', 'monacoEditor.html'],
		['src/editor/monacoHtml.css', 'monacoHtml.css']
		// monacoBundle.js is generated by esbuild — no copy needed
	];
	for (const [src, dest] of files) {
		await smartCopyFile(path.join(pluginDir, src), path.join(buildPath, dest));
	}
}
